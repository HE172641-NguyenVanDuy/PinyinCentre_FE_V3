// Helper function to get exams by level
export const getExamsByLevel = (level) => {
  return hskExamsData[level] || [];
};

// Helper function to get exam by ID
export const getExamById = (id) => {
  for (const level in hskExamsData) {
    const exam = hskExamsData[level].find((exam) => exam.id === parseInt(id));
    if (exam) return exam;
  }
  return null;
};

// Helper function to get all exams
export const getAllExams = () => {
  const allExams = [];
  for (const level in hskExamsData) {
    allExams.push(...hskExamsData[level]);
  }
  return allExams;
};
